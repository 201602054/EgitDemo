package day07.am;

public class MethodDemo1 {
	//定义一个无参数无返回值的方法
		public static void hello() {
			//方法体中输出Hello World!!!
			System.out.println("Hello World !!");
		}
		public static void main(String[] args) {
			System.out.println("aaaaaaaa");
			//调用无参数无返回值的方法
			hello();
			System.out.println("bbbbbbbb");
		}

}
